    //El controlador nos pasa la lista de proveedores accediendo al modelo y los a√±adimos a nuestra JTable
    private void obtenerProveedores(){
        if(controller.obtenerProveedores()!=null){
            LinkedList<Proveedor> listaProveedores = new LinkedList<>(controller.obtenerProveedores());
            modelo.setRowCount(0);
            for (Proveedor proveedor : listaProveedores) {
                Object[] nuevaFila = new Object[1];
                nuevaFila[0] = proveedor.getNombre();
                modelo.addRow(nuevaFila);
            }
        }
    }

