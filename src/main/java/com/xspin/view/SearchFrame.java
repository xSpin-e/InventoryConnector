    private Articulo crearArticulo(int row){
        Articulo articulo = new Articulo();
        articulo.setSku(tabla.getValueAt(row, 0).toString());
        articulo.setNombre(tabla.getValueAt(row,1).toString());
        articulo.setCantidad((int) tabla.getValueAt(row,2));
        articulo.setPrecio(Double.parseDouble(tabla.getValueAt(row,3).toString()));
        articulo.setProveedor(tabla.getValueAt(row,4).toString());
        return articulo;
    }
    
    
