    public void primerosDatos(){
          modelo.setRowCount(0);
        //Crear funci√≥n
       LinkedList<Articulo> listaArticulos = new LinkedList<>(controller.obtenerArticulos());
       for (Articulo articulo : listaArticulos) {
        Object[] nuevaFila = new Object[5];
        nuevaFila[0] = articulo.getSku();
        nuevaFila[1] = articulo.getNombre();
        nuevaFila[2] = articulo.getCantidad();
        nuevaFila[3] = articulo.getPrecio();
        nuevaFila[4] = articulo.getProveedor();
        modelo.addRow(nuevaFila);
       }
    }
    public void consultarDatos(){
        
        
        if(controller.consultarProducto() != null){
            LinkedList<Articulo> listaArticulos = new LinkedList<>(controller.consultarProducto());
            modelo.setRowCount(0);
            for (Articulo articulo : listaArticulos) {
            Object[] nuevaFila = new Object[5];
            nuevaFila[0] = articulo.getSku();
            nuevaFila[1] = articulo.getNombre();
            nuevaFila[2] = articulo.getCantidad();
            nuevaFila[3] = articulo.getPrecio();
            nuevaFila[4] = articulo.getProveedor();
            modelo.addRow(nuevaFila);
        }
        
        }
        
        
    
    }

